$(document).ready(function () {
    $('.dropdown-toggle').mouseover(function() {
        $('.dropdown-menu').show();
    })

    $('.dropdown-toggle').mouseout(function() {
        t = setTimeout(function() {
            $('.dropdown-menu').hide();
        }, 100);

        $('.dropdown-menu').on('mouseenter', function() {
            $('.dropdown-menu').show();
            clearTimeout(t);
        }).on('mouseleave', function() {
            $('.dropdown-menu').hide();
        })
    })
})



// DOM ready
$(function() {
 
  // Create the dropdown base
  $("<select />").appendTo(".nav-tab");

  // Create default option "Go to..."
 /* $("<option />", {
    "selected": "selected",
    "value"   : "",
    "text"    : "Choose Settings"
  }).appendTo(".nav-tab select");*/

  // Populate dropdown with menu items
  $(".nav-tab a").each(function() {
    var el = $(this);
    $("<option />", {
      "value"   : el.attr("href"),
      "text"    : el.text()
    }).appendTo(".nav-tab select");
	
	// To make an option selected by default.
	// For example: eq(0) selects 1st option, eq(1) selects 2nd option and so on...
	$('.nav-tab select option:eq(0)').attr('selected', true);	
	// For modern versions of jquery you should use the .prop() instead of .attr()
	$('.nav-tab select option:eq(0)').prop('selected', true);	
  });

   // To make dropdown actually work
   // To make more unobtrusive: https://css-tricks.com/4064-unobtrusive-page-changer/
  $(".nav-tab select").change(function() {
    window.location = $(this).find("option:selected").val();
  });

});
